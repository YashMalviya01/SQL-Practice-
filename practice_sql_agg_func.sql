/*MIN Queries*/


/*16. Smallest order for each customer.*/



select 
	c.first_name,
	min(o.total_amount) as t
from customers c
join orders o on c.customer_id = o.customer_id
group by c.first_name;



/*17. Cheapest product sold. */


SELECT
    p.product_name,
    MIN(oi.unit_price)
FROM products p
JOIN order_items oi
ON p.product_id = oi.product_id
GROUP BY p.product_name;


/*18. Minimum sale handled by each employee.*/


SELECT
    e.employee_name,
    MIN(o.total_amount)
FROM employees e
JOIN orders o
ON e.employee_id = o.employee_id
GROUP BY e.employee_name;


/*19. Manager with minimum team revenue. (SELF JOIN)*/


SELECT
    m.employee_name,
    MIN(o.total_amount) as mn
FROM employees m
JOIN employees e
ON m.employee_id = e.manager_id
JOIN orders o
ON e.employee_id = o.employee_id
GROUP BY m.employee_name;


